// ─── Rule 9: query/header parameter names must be camelCase ───────────────────

function validateQueryParamNames(filePath, doc) {
  if (!doc?.paths) return;

  // Check path-level and operation-level parameters
  for (const [routePath, pathItem] of Object.entries(doc.paths)) {
    const allParams = [];

    // Path-level parameters
    if (Array.isArray(pathItem.parameters)) {
      allParams.push(...pathItem.parameters);
    }

    // Operation-level parameters
    for (const method of HTTP_METHODS) {
      const op = pathItem[method];
      if (op && Array.isArray(op.parameters)) {
        allParams.push(...op.parameters);
      }
    }

    for (const param of allParams) {
      if (!param || !param.name) continue;
      // Only check query and header params (path params handled by Rule 4)
      if (param.in !== "query" && param.in !== "header") continue;

      const name = param.name;
      const issues = getCamelCaseIssues(name);
      if (issues.length > 0) {
        const suggestion = getCamelCaseSuggestion(name);
        reportStyleIssue(
          filePath,
          `${routePath} — ${param.in} parameter "${name}" ${issues.join("; ")}. ` +
            (suggestion ? `Use camelCase instead: "${suggestion}". ` : "") +
            `See AGENTS.md § "Casing rules at a glance".`,
        );
      }
    }
  }

  // Also check parameter definitions in components
  if (doc.components?.parameters) {
    for (const [paramKey, param] of Object.entries(doc.components.parameters)) {
      if (!param || !param.name) continue;
      if (param.in !== "query" && param.in !== "header") continue;

      const name = param.name;

      const issues = getCamelCaseIssues(name);
      if (issues.length > 0) {
        const suggestion = getCamelCaseSuggestion(name);
        reportStyleIssue(
          filePath,
          `Parameter definition "${paramKey}" — name "${name}" ${issues.join("; ")}. ` +
            (suggestion ? `Use camelCase instead: "${suggestion}".` : ""),
        );
      }
    }
  }
}

// ─── Rule 10: path segments must be kebab-case ───────────────────────────────

function validatePathSegments(filePath, doc) {
  if (!doc?.paths) return;

  for (const routePath of Object.keys(doc.paths)) {
    // Split path into segments, skip empty and parameter segments
    const segments = routePath.split("/").filter((s) => s && !s.startsWith("{"));

    for (const segment of segments) {
      // Skip the "api" prefix — it's a fixed convention
      if (segment === "api") continue;

      // Check for camelCase or PascalCase or snake_case in path segments
      if (!isKebabCase(segment)) {
        const suggestion = segment
          .replace(/([a-z])([A-Z])/g, "$1-$2")
          .replace(/_/g, "-")
          .toLowerCase();
        reportStyleIssue(
          filePath,
          `Path "${routePath}" — segment "${segment}" must be kebab-case. ` +
            `Suggested: "${suggestion}". ` +
            `See AGENTS.md § "Casing rules at a glance".`,
        );
      }
    }
  }
}


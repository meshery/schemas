function detectEnumBaselineRef() {
  const candidates = ["master", "origin/master", "refs/heads/master", "refs/remotes/origin/master", "HEAD^1"];

  for (const ref of candidates) {
    try {
      execFileSync("git", ["rev-parse", "--verify", ref], { cwd: ROOT, stdio: "ignore" });
      return ref;
    } catch (e) {
      continue;
    }
  }

  return null;
}

function loadAdvisoryBaseline() {
  if (!warnOnly || noAdvisoryBaseline) {
    return new Set();
  }

  if (!fs.existsSync(ADVISORY_BASELINE_FILE)) {
    return new Set();
  }

  return new Set(
    fs
      .readFileSync(ADVISORY_BASELINE_FILE, "utf-8")
      .split("\n")
      .map((line) => line.trim())
      .filter((line) => line.length > 0 && !line.startsWith("#"))
  );
}

function recordIssue(file, message, severity = "error") {
  const bucket = severity === "warning" ? advisoryViolations : blockingViolations;
  const relativeFile = path.relative(ROOT, file).split(path.sep).join("/");

  if (severity === "warning") {
    const baselineKey = `${relativeFile}\t${message}`;
    if (advisoryBaseline.has(baselineKey)) {
      return;
    }
  }

  bucket.push({ file: relativeFile, message });
}

function warn(file, message) {
  recordIssue(file, message, "error");
}

function advisory(file, message) {
  recordIssue(file, message, "warning");
}

function isStrictStyleFile(filePath) {
  return filePath.includes(`${path.sep}v1beta2-draft${path.sep}`);
}

function reportStyleIssue(filePath, message) {
  const severity = classifyStyleIssue({
    strictConsistency,
    strictStyleFile: isStrictStyleFile(filePath),
    includeLegacyStyleDebt,
  });

  if (!severity) {
    return;
  }

  recordIssue(filePath, message, severity);
}

function reportDesignAdvisory(filePath, message) {
  recordIssue(filePath, message, classifyDesignIssue({ strictConsistency }));
}

function reportContractAdvisory(filePath, message) {
  const severity = classifyContractIssue({
    strictConsistency,
    strictStyleFile: isStrictStyleFile(filePath),
    includeLegacyContractDebt,
  });

  if (!severity) {
    return;
  }

  recordIssue(filePath, message, severity);
}

// ─── Casing helpers ───────────────────────────────────────────────────────────

/** Returns true if the string is valid camelCase (starts lowercase, no underscores/hyphens). */
function isCamelCase(s) {
  return /^[a-z][a-zA-Z0-9]*$/.test(s);
}

/** Returns true if the string is valid PascalCase (starts uppercase, no underscores/hyphens). */
function isPascalCase(s) {
  return /^[A-Z][a-zA-Z0-9]*$/.test(s);
}

/** Returns true if the string is valid kebab-case (lowercase + hyphens only). */
function isKebabCase(s) {
  return /^[a-z][a-z0-9]*(-[a-z0-9]+)*$/.test(s);
}

/** Returns true if the property name contains an underscore (potential snake_case). */
function hasUnderscore(s) {
  return s.includes("_");
}

/** Returns true if the string contains a SCREAMING_CASE ID token that should be Id. */
function hasScreamingIdToken(s) {
  return /(?:^|[a-z0-9])ID(?:[A-Z0-9]|$)/.test(s);
}

/**
 * Detects common suffixes that are incorrectly all-lowercase in a camelCase name.
 * e.g. "workspaceid" should be "workspaceId", "pageurl" should be "pageUrl".
 *
 * Known safe words excluded: valid, grid, hybrid, liquid, solid, acid, vivid,
 * timid, rapid, humid, lucid, fluid, etc. The approach requires a known
 * compound-word prefix pattern rather than matching all words ending in "id".
 */
const KNOWN_LOWERCASE_SUFFIX_VIOLATIONS = new Set([
  "userid", "orgid", "teamid", "workspaceid", "modelid", "designid",
  "connectionid", "environmentid", "credentialid", "subscriptionid",
  "invitationid", "tokenid", "eventid", "keyid", "roleid", "badgeid",
  "planid", "schemaid", "registrantid", "componentid", "categoryid",
  "pageurl", "avatarurl", "snapshoturl", "callbackurl", "redirecturl",
]);
function hasLowercaseSuffix(s) {
  // Only match the exact all-lowercase form — "orgid" not "orgId"
  return KNOWN_LOWERCASE_SUFFIX_VIOLATIONS.has(s);
}

/**
 * Convert a property name to its expected camelCase form with correct "Id" suffix.
 * Handles snake_case, PascalCase, and SCREAMING_CASE "ID" suffix all at once.
 */
function toCamelCase(s) {
  return s
    .replace(/[^a-zA-Z0-9]+([a-zA-Z0-9])/g, (_, c) => c.toUpperCase())
    .replace(/^([A-Z])/, (m) => m.toLowerCase())
    .replace(/ID(?=[A-Z0-9]|$)/g, "Id");
}

function getCamelCaseSuggestion(name) {
  const suggestion = toCamelCase(name)
    .replace(/^[^a-zA-Z]+/, "")
    .replace(/[^a-zA-Z0-9]/g, "");
  return isCamelCase(suggestion) ? suggestion : null;
}

function getTagBaseValue(value) {
  if (value === undefined || value === null) return null;
  return String(value).replace(/,.*$/, "");
}

function getExtraTags(definition) {
  const extraTags = definition?.["x-oapi-codegen-extra-tags"];
  return extraTags && typeof extraTags === "object" ? extraTags : null;
}

function getDbTagValue(definition) {
  return getTagBaseValue(getExtraTags(definition)?.db);
}

/**
 * Extract the column name from a GORM tag. Handles patterns like:
 * - gorm: "column:model_id"
 * - gorm: "index:...,column:model_id"
 * Returns the column name or null.
 */
function getGormColumnValue(definition) {
  const gormTag = getExtraTags(definition)?.gorm;
  if (typeof gormTag !== "string") return null;
  const match = gormTag.match(/column\s*:\s*([a-z_][a-z0-9_]*)/);
  return match ? match[1] : null;
}

function getJsonTagValue(definition) {
  return getTagBaseValue(getExtraTags(definition)?.json);
}

function isDbBackedSnakeCaseProperty(name, definition) {
  const dbTag = getDbTagValue(definition);
  if (dbTag && DB_TAG_PATTERN.test(dbTag) && hasUnderscore(dbTag) && name === dbTag) return true;
  const gormCol = getGormColumnValue(definition);
  if (gormCol && DB_TAG_PATTERN.test(gormCol) && hasUnderscore(gormCol) && name === gormCol) return true;
  return false;
}

function isAllowedSnakeCaseProperty(name, definition) {
  return DB_MIRRORED_FIELDS.has(name) || isDbBackedSnakeCaseProperty(name, definition);
}

function getCamelCaseIssues(name, { allowDbMirrored = false, definition = null } = {}) {
  if (allowDbMirrored && isAllowedSnakeCaseProperty(name, definition)) return [];

  const issues = [];
  if (hasUnderscore(name)) {
    issues.push("uses snake_case (only DB-backed contract fields may use underscores)");
  }
  if (/^[A-Z]/.test(name)) {
    issues.push("starts with uppercase (must be camelCase, not PascalCase)");
  }
  if (hasScreamingIdToken(name)) {
    issues.push('uses "ID" token (must be "Id")');
  }
  if (hasLowercaseSuffix(name)) {
    // Derive the correct camelCase form from the known violation
    const suffixes = ["ids", "id", "url", "uri"];
    const lc = name.toLowerCase();
    for (const suffix of suffixes) {
      if (lc.endsWith(suffix)) {
        const corrected = name.slice(0, -suffix.length) + suffix.charAt(0).toUpperCase() + suffix.slice(1);
        issues.push(`has all-lowercase suffix "${suffix}" (must be "${corrected}")`);
        break;
      }
    }
  }
  if (/^[0-9]/.test(name)) {
    issues.push("starts with a digit");
  }
  if (/[^a-zA-Z0-9_]/.test(name)) {
    issues.push("contains punctuation not allowed in camelCase");
  }
  if (issues.length === 0 && !isCamelCase(name)) {
    issues.push("is not valid camelCase");
  }

  return issues;
}

function loadYamlDoc(filePath) {
  if (refDocCache.has(filePath)) {
    return refDocCache.get(filePath);
  }

  let doc = null;
  try {
    doc = yaml.load(fs.readFileSync(filePath, "utf-8"));
  } catch (e) {
    doc = null;
  }

  refDocCache.set(filePath, doc);
  return doc;
}

function loadBaselineYamlDoc(filePath) {
  if (baselineDocCache.has(filePath)) {
    return baselineDocCache.get(filePath);
  }

  if (!enumBaselineRef) {
    baselineDocCache.set(filePath, null);
    return null;
  }

  const relativeFile = path.relative(ROOT, filePath).split(path.sep).join("/");
  let doc = null;

  try {
    const source = execFileSync("git", ["show", `${enumBaselineRef}:${relativeFile}`], {
      cwd: ROOT,
      encoding: "utf8",
      stdio: ["pipe", "pipe", "ignore"],
    });
    doc = yaml.load(source);
  } catch (e) {
    doc = null;
  }

  baselineDocCache.set(filePath, doc);
  return doc;
}

function decodeJsonPointerToken(token) {
  return token.replace(/~1/g, "/").replace(/~0/g, "~");
}

function resolveJsonPointer(doc, pointer) {
  if (!doc || !pointer) return null;

  const normalizedPointer = pointer.startsWith("#") ? pointer.slice(1) : pointer;
  if (!normalizedPointer) return doc;
  if (!normalizedPointer.startsWith("/")) return null;

  let current = doc;
  for (const token of normalizedPointer.slice(1).split("/")) {
    current = current?.[decodeJsonPointerToken(token)];
    if (current === undefined) return null;
  }

  return current ?? null;
}

function resolveRefObject(ref, baseFilePath, localDoc) {
  if (!ref || typeof ref !== "string") return null;

  if (ref.startsWith("#/")) {
    return resolveJsonPointer(localDoc, ref);
  }

  const [relativePath, pointer] = ref.split("#");
  if (!relativePath || !pointer) return null;

  const targetPath = path.resolve(path.dirname(baseFilePath), relativePath);
  const targetDoc = loadYamlDoc(targetPath);
  if (!targetDoc) return null;

  return resolveJsonPointer(targetDoc, `#${pointer}`);
}

function resolveParameterName(parameter, filePath, doc) {
  if (parameter?.name) return parameter.name;
  if (!parameter?.$ref) return null;

  const resolvedParameter = resolveRefObject(parameter.$ref, filePath, doc);
  if (resolvedParameter?.name) {
    return resolvedParameter.name;
  }

  return parameter.$ref.split("/").pop() ?? null;
}

// ─── Rule 1: entity schemas must have additionalProperties: false ─────────────

function validateEntitySchema(filePath) {
  let doc;
  try {
    doc = yaml.load(fs.readFileSync(filePath, "utf-8"));
  } catch (e) {
    return; // unparseable — not our concern here
  }

  if (!doc || typeof doc !== "object") return;
  if (doc.type !== "object") return; // skip non-object schemas (enums, etc.)

  if (doc.additionalProperties !== false) {
    warn(
      filePath,
      `Missing \`additionalProperties: false\` at top level. ` +
        `Entity schemas must set this to prevent unknown fields in generated structs.`,
    );
  }

  // Also validate property names in entity schemas
  if (doc.properties) {
    validatePropertyNames(filePath, "(entity root)", doc.properties);
  }

  validateDbBackedPropertyNames(filePath, doc);
}

// ─── Rule 2: POST/PUT requestBody must not use the full entity schema ─────────

function resolveLocalRef(ref, components) {
  // Only handle local component refs: "#/components/schemas/Foo"
  const match = ref && ref.match(/^#\/components\/schemas\/(.+)$/);
  if (!match) return null;
  return components?.schemas?.[match[1]] ?? null;
}

function getRequiredFields(schema, components) {
  if (!schema) return [];
  // Follow a top-level $ref to a local component
  if (schema.$ref) {
    const resolved = resolveLocalRef(schema.$ref, components);
    return resolved ? getRequiredFields(resolved, components) : [];
  }
  return Array.isArray(schema.required) ? schema.required : [];
}

function validateApiSpec(filePath, doc) {
  if (!doc?.paths) return;
  const components = doc.components ?? {};

  for (const [routePath, pathItem] of Object.entries(doc.paths)) {
    for (const method of ["post", "put", "patch"]) {
      const op = pathItem[method];
      if (!op?.requestBody) continue;

      // Collect all content schemas for this requestBody
      const contentMap = op.requestBody.content ?? {};
      for (const [mediaType, mediaObj] of Object.entries(contentMap)) {
        if (!mediaObj?.schema) continue;

        const required = getRequiredFields(mediaObj.schema, components);
        const offending = required.filter((f) => SERVER_GENERATED_FIELDS.has(f));

        if (offending.length > 0) {
          const schemaRef = mediaObj.schema.$ref ?? "(inline)";
          warn(
            filePath,
            `${method.toUpperCase()} ${routePath} — requestBody schema ${schemaRef} ` +
              `has server-generated field(s) in \`required\`: [${offending.join(", ")}]. ` +
              `Use a dedicated *Payload schema that omits server-generated required fields. ` +
              `See AGENTS.md § "The Dual-Schema Pattern".`,
          );
        }
      }
    }
  }
}

// ─── Rule 3: operationId must be lower camelCase with "Id" not "ID" ──────────

// Enforce lower camelCase verbNoun identifiers such as getPatterns.
// This rejects PascalCase, underscores, punctuation, and single-word lowercase IDs.
const OPERATION_ID_RE = /^[a-z][a-z0-9]*(?:[A-Z][a-z0-9]*)+$/;

// Detect SCREAMING_CASE "ID" anywhere in operationId (e.g. ByID, getByID, deleteByID).
// The correct form is always "Id" (e.g. ById, getById, deleteById).
const SCREAMING_ID_RE = /[a-z]ID(?:[A-Z]|$)/;

function validateOperationIds(filePath, doc) {
  if (!doc?.paths) return;

  for (const [routePath, pathItem] of Object.entries(doc.paths)) {
    for (const method of HTTP_METHODS) {
      const op = pathItem[method];
      if (!op?.operationId) continue;

      if (!OPERATION_ID_RE.test(op.operationId)) {
        reportStyleIssue(
          filePath,
          `${method.toUpperCase()} ${routePath} — operationId "${op.operationId}" ` +
            `must use lower camelCase verbNoun without underscores or other separators (e.g. "getPatterns"). ` +
            `See AGENTS.md § "Naming conventions".`,
        );
      } else if (SCREAMING_ID_RE.test(op.operationId) || op.operationId.endsWith("ID")) {
        const suggestion = op.operationId.replace(/ID(?=[A-Z]|$)/g, "Id");
        reportStyleIssue(
          filePath,
          `${method.toUpperCase()} ${routePath} — operationId "${op.operationId}" ` +
            `uses "ID" suffix instead of "Id". Use: "${suggestion}". ` +
            `The "Id" casing is universal across all identifiers. ` +
            `See AGENTS.md § "Casing rules at a glance".`,
        );
      }
    }
  }
}

// ─── Rule 4: path parameters must be camelCase with Id suffix ─────────────────

const PATH_PARAM_RE = /\{([^}]+)\}/g;

function isBadPathParam(param) {
  // Path params should use lower camelCase.
  if (/^[A-Z]/.test(param)) return true;
  // Flag SCREAMING_CASE suffix: ends with ID (two uppercase letters)
  if (param.endsWith("ID")) return true;
  // Keep plain legacy {id} allowed, but flag lowercase id suffixes like orgid;
  // canonical form for multiword names is orgId.
  if (param === "id") return false;
  if (/id$/.test(param) && !param.endsWith("Id")) return true;
  // Flag snake_case: contains underscore
  if (/_/.test(param)) return true;
  return false;
}

function suggestPathParam(param) {
  if (param === "id") return param;

  const normalized = param
    .replace(/[_-]+([a-zA-Z0-9])/g, (_, c) => c.toUpperCase())
    .replace(/^([A-Z])/, (match) => match.toLowerCase());

  return normalized.replace(/(?:ID|id)$/, "Id");
}

function validatePathParams(filePath, doc) {
  if (!doc?.paths) return;

  for (const routePath of Object.keys(doc.paths)) {
    let match;
    PATH_PARAM_RE.lastIndex = 0;
    while ((match = PATH_PARAM_RE.exec(routePath)) !== null) {
      const param = match[1];
      if (isBadPathParam(param)) {
        const suggestion = suggestPathParam(param);
        reportStyleIssue(
          filePath,
          `Path "${routePath}" — parameter {${param}} uses incorrect casing. ` +
            `Use camelCase with "Id" suffix: {${suggestion}}. ` +
            `See AGENTS.md § "Naming conventions".`,
        );
      }
    }
  }
}

// ─── Rule 5: DELETE must not have a requestBody ───────────────────────────────

function validateDeleteNoBody(filePath, doc) {
  if (!doc?.paths) return;

  for (const [routePath, pathItem] of Object.entries(doc.paths)) {
    const op = pathItem["delete"];
    if (!op) continue;

    if (op.requestBody) {
      warn(
        filePath,
        `DELETE ${routePath} — DELETE operations must not have a requestBody. ` +
          `Use a POST sub-resource for bulk deletes (e.g. POST ${routePath}/delete). ` +
          `See AGENTS.md § "HTTP API Design Principles".`,
      );
    }
  }
}

// ─── Rule 6: schema property names must be camelCase ──────────────────────────

/**
 * Validates that all property names in a schema object follow casing rules:
 * - camelCase for new non-DB properties
 * - exact snake_case DB names for DB-backed contract fields
 * - Flags PascalCase, SCREAMING_CASE, and non-authoritative snake_case
 */
function validatePropertyNames(filePath, schemaName, properties) {
  if (!properties || typeof properties !== "object") return;

  for (const [propName, propDef] of Object.entries(properties)) {
    // Skip $ref-only properties (no name to validate)
    if (propName.startsWith("$")) continue;

    const issues = getCamelCaseIssues(propName, { allowDbMirrored: true, definition: propDef });
    if (issues.length > 0) {
      const suggestion = getCamelCaseSuggestion(propName);
      reportStyleIssue(
        filePath,
        `Schema "${schemaName}" — property "${propName}" ${issues.join("; ")}. ` +
          (suggestion ? `Use: "${suggestion}". ` : "") +
          `See AGENTS.md § "Casing rules at a glance".`,
      );
    }
  }
}

function validateAllSchemaProperties(filePath, doc) {
  if (!doc?.components?.schemas) return;

  for (const [schemaName, schemaDef] of Object.entries(doc.components.schemas)) {
    if (!schemaDef || typeof schemaDef !== "object") continue;

    // Validate properties of this schema
    if (schemaDef.properties) {
      validatePropertyNames(filePath, schemaName, schemaDef.properties);
    }

    // Also validate nested allOf/oneOf/anyOf schemas
    for (const combiner of ["allOf", "oneOf", "anyOf"]) {
      if (Array.isArray(schemaDef[combiner])) {
        for (const subSchema of schemaDef[combiner]) {
          if (subSchema?.properties) {
            validatePropertyNames(filePath, schemaName, subSchema.properties);
          }
        }
      }
    }
  }
}

// ─── Rule 32: DB-backed property names must match db tags exactly ────────────

function validateDbBackedPropertyNames(filePath, doc) {
  if (!doc || typeof doc !== "object") return;

  if (doc.properties) {
    validateDbBackedPropertyTree(filePath, "(root)", doc);
  }

  if (!doc?.components?.schemas) return;

  for (const [schemaName, schemaDef] of Object.entries(doc.components.schemas)) {
    validateDbBackedPropertyTree(filePath, schemaName, schemaDef);
  }
}

function validateDbBackedPropertyTree(filePath, scope, schema) {
  if (!schema || typeof schema !== "object") return;

  if (schema.properties && typeof schema.properties === "object") {
    for (const [propName, propDef] of Object.entries(schema.properties)) {
      if (!propDef || typeof propDef !== "object") continue;

      const dbValue = getDbTagValue(propDef);
      const gormCol = getGormColumnValue(propDef);
      const columnName = dbValue || gormCol;
      const jsonValue = getJsonTagValue(propDef);

      // If property name differs from column name but json tag already matches the column,
      // this is a deliberate semantic alias (e.g. registrantId with json:"connection_id") — skip.
      const jsonMatchesColumn = jsonValue && jsonValue !== "-" && jsonValue === columnName;
      if (columnName && DB_TAG_PATTERN.test(columnName) && hasUnderscore(columnName) && propName !== columnName && !jsonMatchesColumn) {
        const tagSource = dbValue ? "db" : "gorm column";
        warn(
          filePath,
          `Schema "${scope}" — property "${propName}" maps to database column "${columnName}" (via ${tagSource} tag). ` +
            `DB-backed property names are stable wire-format fields and must use the exact snake_case db name. ` +
            `Rename the schema property to "${columnName}" or introduce a new API version for a full casing migration.`,
        );
      }

      if (columnName && DB_TAG_PATTERN.test(columnName) && hasUnderscore(columnName) && jsonValue && jsonValue !== "-" && jsonValue !== columnName) {
        const tagSource = dbValue ? "db" : "gorm column";
        warn(
          filePath,
          `Schema "${scope}" — property "${propName}" has json tag "${jsonValue}" but ${tagSource} tag "${columnName}". ` +
            `DB-backed property names and JSON tags must both use the exact snake_case db name in this API version.`,
        );
      }

      validateDbBackedPropertyTree(filePath, `${scope}.${propName}`, propDef);
    }
  }

  for (const combiner of ["allOf", "oneOf", "anyOf"]) {
    if (Array.isArray(schema[combiner])) {
      schema[combiner].forEach((subSchema, index) => {
        validateDbBackedPropertyTree(filePath, `${scope}.${combiner}[${index}]`, subSchema);
      });
    }
  }

  if (schema.items) {
    validateDbBackedPropertyTree(filePath, `${scope}.items`, schema.items);
  }
}

// ─── Rule 33: pagination envelopes use page_size / total_count ───────────────

function validatePaginationEnvelopeFieldNames(filePath, doc) {
  if (!doc?.components?.schemas) return;

  for (const [schemaName, schemaDef] of Object.entries(doc.components.schemas)) {
    validatePaginationEnvelopeTree(filePath, schemaName, schemaDef);
  }
}

function validatePaginationEnvelopeTree(filePath, scope, schema) {
  if (!schema || typeof schema !== "object") return;

  if (schema.properties && typeof schema.properties === "object") {
    const propertyNames = new Set(Object.keys(schema.properties));
    const looksPaginated =
      propertyNames.has("page") ||
      propertyNames.has("pageSize") ||
      propertyNames.has("page_size") ||
      propertyNames.has("totalCount") ||
      propertyNames.has("total_count");

    if (looksPaginated) {
      if (propertyNames.has("pageSize")) {
        warn(
          filePath,
          `Schema "${scope}" — pagination envelopes must use "page_size", not "pageSize". ` +
            `These response fields are part of the published API contract.`,
        );
      }
      if (propertyNames.has("totalCount")) {
        warn(
          filePath,
          `Schema "${scope}" — pagination envelopes must use "total_count", not "totalCount". ` +
            `These response fields are part of the published API contract.`,
        );
      }
    }

    for (const [propName, propDef] of Object.entries(schema.properties)) {
      validatePaginationEnvelopeTree(filePath, `${scope}.${propName}`, propDef);
    }
  }

  for (const combiner of ["allOf", "oneOf", "anyOf"]) {
    if (Array.isArray(schema[combiner])) {
      schema[combiner].forEach((subSchema, index) => {
        validatePaginationEnvelopeTree(filePath, `${scope}.${combiner}[${index}]`, subSchema);
      });
    }
  }

  if (schema.items) {
    validatePaginationEnvelopeTree(filePath, `${scope}.items`, schema.items);
  }
}


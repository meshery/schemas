#!/usr/bin/env node
/**
 * validate-schemas.js — Schema Design Validation
 *
 * DESCRIPTION:
 *   Validates schema contracts and generator-sensitive patterns documented in
 *   AGENTS.md. Blocking failures are reserved for issues that break code generation,
 *   violate the dual-schema pattern, or make contracts structurally unsafe. Legacy
 *   style/design audits remain available via --warn so existing v1beta1 constructs
 *   can be migrated incrementally without breaking the default validation flow.
 *
 *   Rule  1 — Entity schemas (<construct>.yaml) must have `additionalProperties: false`.
 *   Rule  2 — POST/PUT requestBody schemas must not contain server-generated fields
 *              in their `required` array (use a dedicated *Payload schema).
 *   Rule  3 — `operationId` must be lower camelCase verbNoun.
 *   Rule  4 — Path parameters must be camelCase with "Id" suffix.
 *   Rule  5 — DELETE operations must not have a requestBody.
 *   Rule  6 — Schema property names must preserve published casing: DB-backed properties
 *              use exact snake_case db names; new non-DB properties use camelCase.
 *   Rule  7 — Schema component names (components/schemas keys) must be PascalCase.
 *   Rule  8 — Newly introduced enum values must be lowercase.
 *   Rule  9 — Query/header parameter names must be camelCase.
 *   Rule 10 — Path segments must be kebab-case.
 *   Rule 11 — x-generate-db-helpers must be at schema component level, not per-property.
 *   Rule 12 — api.yml must declare openapi: 3.0.x (not 3.1.0).
 *   Rule 13 — api.yml must have info.title and info.version.
 *   Rule 14 — x-internal must be omitted or ["cloud"] / ["meshery"].
 *   Rule 15 — Cross-construct $ref in schema properties must have x-go-type + x-go-type-import.
 *   Rule 16 — x-go-type alias prefix must match x-go-type-import.name.
 *   Rule 17 — x-go-type: "core.Map" must pair with x-go-type-skip-optional-pointer: true.
 *   Rule 18 — Each construct must have template files in templates/ subdirectory.
 *   Rule 19 — No unnecessary single-entry allOf wrappers on ordinary properties.
 *   Rule 20 — Entity schema must have both properties and required sections.
 *   Rule 21 — GET responses must not reference *Payload schemas.
 *   Rule 22 — Manual *_helper.go files must contain "not autogenerated" marker.
 *   Rule 23 — Every operation must define standard error responses (401, 500 minimum).
 *   Rule 24 — api.yml with path operations must declare a security scheme.
 *   Rule 25 — List endpoints (GET returning arrays) must reference standard pagination parameters.
 *   Rule 26 — Inline schemas with 4+ properties should be extracted to components/schemas.
 *   Rule 27 — x-oapi-codegen-extra-tags db: values must be snake_case; json: must match property name.
 *   Rule 28 — HTTP response codes must match method semantics (201 for create, 204 for delete).
 *   Rule 29 — Duplicate component schemas across constructs should use $ref instead.
 *   Rule 30 — Response schemas should use $ref to components/schemas, not inline definitions.
 *   Rule 31 — Response descriptions and inline response message text must not include the word "successfully".
 *   Rule 32 — DB-backed property names must exactly match snake_case db tags.
 *   Rule 33 — Pagination envelopes must use page, page_size, total_count.
 *   Rule 34 — Template file values must match schema property types.
 *   Rule 35 — x-go-type alias must match x-go-type-import.name, and import path must match alias.
 *   Rule 36 — Every schema property should have a `description` field.
 *   Rule 37 — Every operation must define at least one OpenAPI tag, and when document-root `tags:` are declared operation tags must reference one of those definitions.
 *   Rule 38 — String properties (without $ref or enum) should have at least one validation
 *              constraint: minLength, maxLength, pattern, or format.
 *   Rule 39 — Integer/number properties should have minimum and/or maximum bounds,
 *              or use const to pin a single allowed value.
 *   Rule 40 — String properties named *id/*Id must have format: uuid or $ref to a UUID schema.
 *              Skips non-string types and properties annotated with `x-id-format: external`.
 *   Rule 41 — Page-size properties (page_size, pagesize) must have minimum: 1.
 *
 * USAGE:
 *   node build/validate-schemas.js          # exits 0 if no blocking violations found
 *   node build/validate-schemas.js --warn   # also prints non-blocking advisories and exits 0
 *   node build/validate-schemas.js --warn --no-baseline   # prints full actionable advisory backlog
 *   node build/validate-schemas.js --warn --no-baseline --style-debt   # includes legacy style debt too
 *   node build/validate-schemas.js --warn --no-baseline --style-debt --contract-debt   # includes all legacy debt
 *   node build/validate-schemas.js --strict-consistency --style-debt --contract-debt   # fails on all style/design/contract debt
 *
 * DEPENDENCIES:
 *   js-yaml (already a project dependency)
 */


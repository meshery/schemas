#!/usr/bin/env node
/**
 * generate-site-data.js - Build the site data for schemas.meshery.io.
 */

const fs = require("fs");
const path = require("path");
const yaml = require("js-yaml");

const logger = require("./lib/logger");
const paths = require("./lib/paths");

const CONSTRUCTS_ROOT = paths.fromRoot("schemas", "constructs");
const SITE_DATA_DIR = paths.fromRoot("site", "_data");

function parseVersion(version) {
  const match = /^v(\d+)(alpha|beta)(\d+)$/i.exec(version);
  if (!match) {
    return null;
  }

  return {
    major: Number(match[1]),
    stage: match[2].toLowerCase(),
    stageRank: match[2].toLowerCase() === "beta" ? 1 : 0,
    stageNumber: Number(match[3]),
  };
}

function compareVersionsDesc(left, right) {
  const a = parseVersion(left);
  const b = parseVersion(right);

  if (!a || !b) {
    return right.localeCompare(left);
  }

  if (a.major !== b.major) {
    return b.major - a.major;
  }
  if (a.stageRank !== b.stageRank) {
    return b.stageRank - a.stageRank;
  }
  if (a.stageNumber !== b.stageNumber) {
    return b.stageNumber - a.stageNumber;
  }
  return right.localeCompare(left);
}

function listVersionDirectories(rootDir) {
  if (!fs.existsSync(rootDir)) {
    return [];
  }

  return fs
    .readdirSync(rootDir, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name)
    .sort(compareVersionsDesc);
}

function listConstructDirectories(rootDir, version) {
  const versionDir = path.join(rootDir, version);
  if (!fs.existsSync(versionDir)) {
    return [];
  }

  return fs
    .readdirSync(versionDir, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b));
}

function discoverConstructEntries(rootDir = CONSTRUCTS_ROOT) {
  const entries = [];

  for (const version of listVersionDirectories(rootDir)) {
    for (const name of listConstructDirectories(rootDir, version)) {
      entries.push({ name, version });
    }
  }

  return entries;
}

function buildLatestInventory(entries) {
  const grouped = new Map();

  for (const entry of entries) {
    if (!grouped.has(entry.name)) {
      grouped.set(entry.name, []);
    }
    grouped.get(entry.name).push(entry.version);
  }

  return [...grouped.entries()]
    .sort(([left], [right]) => left.localeCompare(right))
    .map(([name, versions]) => {
      const sortedVersions = [...versions].sort(compareVersionsDesc);
      return {
        name,
        version: sortedVersions[0],
        versions: sortedVersions,
      };
    });
}

function writeYamlFile(filePath, data) {
  const header = "# auto-generated by node build/generate-site-data.js — do not edit\n";
  const body = yaml.dump(data, {
    lineWidth: -1,
    noRefs: true,
    sortKeys: false,
  });

  paths.writeFile(filePath, `${header}${body}`);
}

function generateSiteData(rootDir = CONSTRUCTS_ROOT, outputDir = SITE_DATA_DIR) {
  const entries = discoverConstructEntries(rootDir);
  const latest = buildLatestInventory(entries);

  const outputs = {
    constructs: path.join(outputDir, "constructs.yml"),
    constructsAll: path.join(outputDir, "constructs_all.yml"),
    constructsLatest: path.join(outputDir, "constructs_latest.yml"),
  };

  writeYamlFile(outputs.constructs, entries);
  writeYamlFile(outputs.constructsAll, entries);
  writeYamlFile(outputs.constructsLatest, latest);

  return { entries, latest, outputs };
}

function main() {
  logger.step("Generating site construct inventories");
  const { entries, latest, outputs } = generateSiteData();
  logger.success(`Generated ${entries.length} versioned construct entries`);
  logger.success(`Generated ${latest.length} construct summaries`);
  logger.outputFiles(Object.values(outputs).map((file) => paths.relativePath(file)));
}

if (require.main === module) {
  try {
    main();
  } catch (error) {
    logger.error(error.message);
    process.exit(1);
  }
}

module.exports = {
  buildLatestInventory,
  compareVersionsDesc,
  discoverConstructEntries,
  generateSiteData,
  parseVersion,
};
